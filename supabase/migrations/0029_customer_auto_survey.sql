alter table public.customers
  add column if not exists auto_survey_enabled boolean not null default false;

comment on column public.customers.auto_survey_enabled is
  'true = ให้ scan-invitations cron ส่งแบบประเมินรอบอัตโนมัติ (A/B) กับลูกค้ารายนี้; false (ค่าเริ่มต้น) = ข้ามรอบอัตโนมัติ (ยังส่งเองผ่านปุ่ม/เซล C/D ได้)';
